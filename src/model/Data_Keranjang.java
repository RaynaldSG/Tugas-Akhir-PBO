package model;

public class Data_Keranjang {
    private Integer id_kamera;
    private String model;
    private String merek;
    private Integer jumlah;
    private Integer price;
    private Integer hari;
    private Integer total;

    public Integer getId_kamera() {
        return id_kamera;
    }

    public void setId_kamera(Integer id_kamera) {
        this.id_kamera = id_kamera;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getMerek() {
        return merek;
    }

    public void setMerek(String merek) {
        this.merek = merek;
    }

    public Integer getJumlah() {
        return jumlah;
    }

    public void setJumlah(Integer jumlah) {
        this.jumlah = jumlah;
    }

    public Integer getPrice() {
        return price;
    }

    public void setPrice(Integer price) {
        this.price = price;
    }

    public Integer getHari() {
        return hari;
    }

    public void setHari(Integer hari) {
        this.hari = hari;
    }

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

    
    
}
